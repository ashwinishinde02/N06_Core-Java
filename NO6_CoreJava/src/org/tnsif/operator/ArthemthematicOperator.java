package org.tnsif.operator;

import java.util.Scanner;

public class ArthemthematicOperator {

	public static void main(String[] args) {
		Scanner s=new Scanner(System.in);
		int num1=s.nextInt();
		int num2=s.nextInt();
		System.out.print(num1+num2);
		System.out.print(num1-num2);
		System.out.print(num1*num2);
		System.out.print(num1/num2);
		System.out.print(num1%num2);

		
		
	}

}
